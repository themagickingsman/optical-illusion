import { HarmonicBody } from '../HarmonicResonanceEngine';
import { Units } from './unit_converter';
import nucleobases from '../datasets/dna/nucleobases.json';
import aminoAcids from '../datasets/dna/amino_acids.json';
import codons from '../datasets/dna/codons.json';

/**
 * ── COSMIC COMPASS DNA CATALOG (OCTAVE 5 & 6) ──
 * This catalog abandons generic biological labels and maps raw DNA elements
 * (Nucleobases, Amino Acids, Chromosomes) as fundamental physical frequencies.
 * 
 * Logic Mapping:
 * - 1 Molar Mass (g/mol) = ~1 Da = 1.660539x10^-27 kg
 * - DNA Base Pair Length: 0.34 nm
 * - Octave 5: Molecular Bonds / Chemical Resonances
 * - Octave 6: Bio-Molecular / Chromosomal Architecture
 */

export const getDnaElements = (): HarmonicBody[] => {
    const idCounter = 5000;
    const bodies: HarmonicBody[] = [];

    // --- 1. THE 4 FUNDAMENTAL NUCLEOBASES ---
    // Mapping their physical mass to their visual representation.
    // Base Pair Length = 0.34 nm -> mapped as an inner orbit constraint.

    const baseColors: Record<string, string> = {
        'A': '#facc15', // Adenine (Gold)
        'T': '#3b82f6', // Thymine (Blue)
        'C': '#ef4444', // Cytosine (Red)
        'G': '#10b981', // Guanine (Emerald)
        'U': '#8b5cf6', // Uracil (Violet)
    };

    nucleobases.forEach((base: any) => {
        // Map molecular mass to a frequency equivalent (Inverse relationship mapping for demo)
        // Heavier molecules have slower physical resonant frequencies.
        const frequency_hz = 1000 / base.mass_gmol; 

        bodies.push({
            id: `dna-base-${base.symbol}`,
            name: `${base.name} (${base.formula})`,
            // Set at the structural limit of Octave 5 (0.34 nm is the base pair gap)
            radius_au: Units.fromNanometers(0.34),
            period_sec: base.mass_gmol * 86400, // scaled
            color: baseColors[base.symbol] || '#ffffff',
            inclination: 0,
            freq_hz: frequency_hz,
            orbital_freq: 1 / (base.mass_gmol * 86400),
            initial_phase: 0, 

            level_ref: 50,
            eccentricity: 0,
            geometry: 'Circle',
            precession_rate: 0,
            drag_factor: 1,
            body_radius: 2,
            consciousness: '', core_desire: '', archetype: '',
            spin_freq: 0,
            perturbation: { type: 'sine', strength: 0 }
        });
    });

    // --- 2. THE 20 AMINO ACIDS ---
    // Mapped slightly further out as macro-structures generated by the codons.

    aminoAcids.forEach((aa: any) => {
        const frequency_hz = 2000 / (aa.mass || 100);

        bodies.push({
            id: `amino-${aa.abbr}`,
            name: `A.A. ${aa.name} (${aa.abbr})`,
            // Mapped at ~1.0 nm -> folded protein start scale
            radius_au: Units.fromNanometers(1.0 + (aa.mass / 200)), 
            period_sec: aa.mass * 86400,
            color: '#a855f7', // Violet for Amino Acids
            inclination: (aa.mass % 30), // Geometric spread
            freq_hz: frequency_hz,
            orbital_freq: 1 / (aa.mass * 86400),
            initial_phase: 0, 
            level_ref: 60,
            eccentricity: 0,
            geometry: 'Circle',
            precession_rate: 0,
            drag_factor: 1,
            body_radius: 2,
            consciousness: '', core_desire: '', archetype: '',
            spin_freq: 0,
            perturbation: { type: 'sine', strength: 0 }
        });
    });

    // --- 3. THE FULL 64 CODON GEOMETRIC MATRIX ---
    // Mathematically generating the 64-point standing wave using Base-4 polarity.
    
    const baseToVal: Record<string, number> = { 'A': 0, 'C': 1, 'G': 2, 'T': 3, 'U': 3 };
    const baseToBonds: Record<string, number> = { 'A': 2, 'C': 3, 'G': 3, 'T': 2, 'U': 2 };

    codons.forEach((codon: any) => {
        // Base-4 combinatorial index (0-63)
        const v0 = baseToVal[codon.sequence[0]] || 0;
        const v1 = baseToVal[codon.sequence[1]] || 0;
        const v2 = baseToVal[codon.sequence[2]] || 0;
        const decimalIndex = v0 * 16 + v1 * 4 + v2 * 1;
        
        // 360-degree spread based on index
        const angleDeg = (decimalIndex / 64) * 360;
        
        // Polarity (Total Hydrogen Bonds = 6 to 9)
        const totalBonds = 
            (baseToBonds[codon.sequence[0]] || 2) +
            (baseToBonds[codon.sequence[1]] || 2) +
            (baseToBonds[codon.sequence[2]] || 2);

        // Frequency scales with polarity ties
        const baseFreq = 100 * totalBonds; 

        bodies.push({
            id: `codon-${codon.sequence}`,
            name: `Codon [${codon.sequence}] -> ${codon.amino_acid}`,
            radius_au: Units.fromNanometers(1.02),
            period_sec: 64 * 86400, // Wheel constraint
            color: codon.amino_acid === 'Stop' ? '#ef4444' : (codon.sequence === 'ATG' ? '#10b981' : '#38bdf8'),
            inclination: 0, 
            freq_hz: baseFreq, 
            orbital_freq: 1 / (64 * 86400),
            initial_phase: angleDeg * (Math.PI / 180), // Perfect 64-point distribution
            level_ref: 55,
            eccentricity: 0,
            geometry: 'Circle',
            precession_rate: 0,
            drag_factor: 1,
            body_radius: totalBonds === 9 ? 3 : (totalBonds === 6 ? 1.5 : 2), // Size based on bond strength
            consciousness: '', core_desire: '', archetype: '',
            spin_freq: 0,
            perturbation: { type: 'sine', strength: 0 }
        });
    });

    return bodies;
};
